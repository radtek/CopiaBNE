        #region Propriedades
        private Thread _objThread;
        private static DateTime _dataHoraUltimaExecucao;
        #endregion

        #region Eventos

        #region OnStart
        protected override void OnStart(string[] args)
        {
            var objTs = new ThreadStart(Iniciar);
            _objThread = new Thread(objTs);
            _objThread.Start();
        }
        #endregion

        #region OnStop
        protected override void OnStop()
        {
            if (_objThread != null)
                _objThread.Abort();
        }
        #endregion

        #endregion

        #region Metodos

        #region Iniciar
        public void Iniciar()
        {
            try
            {
                Settings.Default.Reload();
                AjustarThread(DateTime.Now, true);

                while (true)
                {
                    EventLogWriter.LogEvent(string.Format("Importação/Exportação de vagas de vagas Iniciada: {0}.", DateTime.Now), EventLogEntryType.Information, Event.InicioExecucao);
                    _dataHoraUltimaExecucao = DateTime.Now;

                    foreach (var objOrigemImportacao in Integrador.RecuperarIntegradoresAtivos())
                    {
                        try
                        {
                            RealizarImportacao(objOrigemImportacao);
                        }
                        catch (Exception ex)
                        {
                            string message;
                            var id = GerenciadorException.GravarExcecao(ex, out message);
                            message = string.Format("{0} - {1}", id, message);
                            EventLogWriter.LogEvent(message, EventLogEntryType.Error, Event.ErroExecucao);
                        }
                    }

                    EventLogWriter.LogEvent(string.Format("Terminou agora {0}.", DateTime.Now), EventLogEntryType.Information, Event.FimExecucao);
                    AjustarThread(DateTime.Now, false);
                }
            }
            catch (Exception ex)
            {
                string message;
                var id = GerenciadorException.GravarExcecao(ex, out message);
                message = string.Format("{0} - {1}", id, message);
                EventLogWriter.LogEvent(message, EventLogEntryType.Error, Event.ErroExecucao);
            }
        }

        #region RealizarImportacao
        private void RealizarImportacao(Integrador objIntegrador)
        {
            //Recupera todas as vagas do XML
            var vagas = Service.EfetuarRequisicao(objIntegrador);
            var idsVagasAtivas = new List<Vaga>();
            var MensagemSucessoImportacao = Parametro.RecuperaValorParametro(BLL.Enumeradores.Parametro.MensagemSucessoImportacao);
            var MensagemErroImportacao = Parametro.RecuperaValorParametro(BLL.Enumeradores.Parametro.MensagemErroImportacao);
            var UrlIntegracao = objIntegrador.GetValorParametro(BLL.Enumeradores.Parametro.Integracao_Url_Integracao);

            var lstDisponibilidadesNovas = new List<VagaDisponibilidade>();
            var lstDisponibilidadesExcluidas = new List<VagaDisponibilidade>();
            var lstTipoVinculoNovos = new List<VagaTipoVinculo>();
            var lstTipoVinculoExcluidos = new List<VagaTipoVinculo>();

            #region Definindo Origem
            //Recuperando parametrização.
            var sIdOrigem = objIntegrador.GetValorParametro(BLL.Enumeradores.Parametro.Integracao_Idf_Origem_Vaga);
            int idOrigem;
            Origem origem;

            //Verifica parametrização do integrador
            if (!string.IsNullOrEmpty(sIdOrigem) && int.TryParse(sIdOrigem, out idOrigem))
            {
                origem = new Origem(idOrigem);
            }
            else
            {
                //Recupera a origem da filial do Integrador para definir corretamente a Origem
                var idOrigemDaFilial = OrigemFilial.RecuperarIdOrigemPorFilial(objIntegrador.Filial);
                origem = idOrigemDaFilial.HasValue ? new Origem(idOrigemDaFilial.Value) : new Origem(1); //Se não foi encontrada origem para a filial, a origem será o BNE
            }
            #endregion Definindo Origem

            //Configurando envio de e-mails de candidatos
            var sFlag = objIntegrador.GetValorParametro(BLL.Enumeradores.Parametro.Integracao_Flg_Receber_Cada_CV);
            var FlagReceberCadaCV = sFlag == "1" || sFlag == "true";
            sFlag = objIntegrador.GetValorParametro(BLL.Enumeradores.Parametro.Integracao_Flg_Receber_Todos_CV);
            var FlagReceberTodosCV = sFlag == "1" || sFlag == "true";

            foreach (var objVagaIntegracao in vagas)
            {
                try
                {
                    //Se a cidade não foi definida, então não realiza importação
                    if (objVagaIntegracao.Vaga.Cidade == null)
                        continue;

                    //Verifica se a vaga pode ser publicada automaticamente
                    var erroPublicacaoAutomatica = VerificarPublicacaoAutomatica(objVagaIntegracao);

                    //Variável que define se o processo de e-mail do publicador de vaga tem que ser executado
                    var flgInicializaPublicadorDeVagas = false;

                    VagaIntegracao objVagaIntegracaoBD;
                    var gravarHistorico = true;

                    //Inicializando listas de disponibilidades e tipo vinculo

                    lstDisponibilidadesNovas.Clear();
                    lstDisponibilidadesExcluidas.Clear();
                    lstTipoVinculoNovos.Clear();
                    lstTipoVinculoExcluidos.Clear();

                    //Verificando a existencia da vaga
                    if (!VagaIntegracao.CarregarPorCodigoIntegrador(objVagaIntegracao.CodigoVagaIntegrador, objIntegrador, out objVagaIntegracaoBD))
                    {
                        #region VagaIntegracaoNaoCarregada
                        //Se a vaga está inativa no arquivo, não realiza a importação
                        if (objVagaIntegracao.Vaga.FlagInativo)
                        {
                            continue;
                        }

                        //Se funcao não foi identificada tenta detectar funcao através da string recebida.
                        if (objVagaIntegracao.Vaga.Funcao == null)
                        {
                            objVagaIntegracao.Vaga.Funcao = Funcao.DetectarFuncao(objVagaIntegracao.Vaga.DescricaoFuncao);
                        }

                        //Se não existir, cria uma nova vaga
                        objVagaIntegracaoBD = new VagaIntegracao
                        {
                            CodigoVagaIntegrador = objVagaIntegracao.CodigoVagaIntegrador,
                            Integrador = objIntegrador,
                            Vaga = objVagaIntegracao.Vaga,
                            FlagEnviadaParaAuditoria = false
                        };

                        //Inicializando flags
                        objVagaIntegracaoBD.Vaga.FlagAuditada = false;
                        objVagaIntegracaoBD.Vaga.FlagBNERecomenda = false;
                        objVagaIntegracaoBD.Vaga.FlagVagaArquivada = false;
                        objVagaIntegracaoBD.Vaga.FlagVagaMassa = false;
                        objVagaIntegracaoBD.Vaga.FlagLiberada = false;

                        //Configurando envio de e-mails de candidatos
                        objVagaIntegracao.Vaga.FlagReceberCadaCV = FlagReceberCadaCV;
                        objVagaIntegracao.Vaga.FlagReceberTodosCV = FlagReceberTodosCV;

                        //Setando Filial e UsuarioFilialPerfil
                        objVagaIntegracaoBD.Vaga.Filial = objIntegrador.Filial;
                        objVagaIntegracaoBD.Vaga.UsuarioFilialPerfil = objIntegrador.UsuarioFilialPerfil;

                        //Setando todas as disponibilidades como novas
                        if (objVagaIntegracao.Disponibilidades != null)
                        {
                            lstDisponibilidadesNovas.AddRange(objVagaIntegracao.Disponibilidades);
                        }
                        if (objVagaIntegracao.TiposVinculo != null)
                        {
                            lstTipoVinculoNovos = objVagaIntegracao.TiposVinculo;
                        }

                        //Verifica se a vaga pode ser publicada automaticamente
                        if (string.IsNullOrEmpty(erroPublicacaoAutomatica))
                        {
                            objVagaIntegracaoBD.Vaga.DataAbertura = DateTime.Now;
                            objVagaIntegracaoBD.Vaga.DataPrazo = DateTime.Now.AddDays(Convert.ToInt32(Parametro.RecuperaValorParametro(BLL.Enumeradores.Parametro.QuantidadeDiasPrazoVaga)));

                            //Vaga importada pela primeira vez publicada automaticamente
                            //Inicializa Publicador de vagas
                            flgInicializaPublicadorDeVagas = true;
                        }
                        else
                        {
                            objVagaIntegracaoBD.Vaga.DataAbertura = null;
                            objVagaIntegracaoBD.Vaga.DataPrazo = null;
                        }
                        #endregion VagaIntegracaoNaoCarregada
                    }
                    else
                    {
                        #region VagaIntegracaoCarregada
                        objVagaIntegracaoBD.Vaga.CompleteObject();

                        //Se a vaga está inativa no arquivo, inativa a vaga no bne caso esta ainda esteja ativa e cancela outras alterações.
                        if (objVagaIntegracao.Vaga.FlagInativo && !objVagaIntegracaoBD.Vaga.FlagInativo)
                        {
                            if (!objVagaIntegracaoBD.Vaga.FlagInativo)
                            {
                                objVagaIntegracaoBD.Vaga.FlagInativo = true;
                                objVagaIntegracaoBD.Vaga.Save(null, BLL.Enumeradores.VagaLog.ImportarVagas);
                            }
                            continue;
                        }

                        //Se a vaga foi excluída ou auditada no BNE, não realiza nenhuma alteração.
                        if ((objVagaIntegracaoBD.Vaga.FlagAuditada.HasValue && objVagaIntegracaoBD.Vaga.FlagAuditada.Value)
                            || objVagaIntegracaoBD.Vaga.FlagInativo || objVagaIntegracaoBD.Vaga.FlagVagaArquivada)
                        {
                            if (!objVagaIntegracaoBD.Vaga.FlagInativo && !objVagaIntegracaoBD.Vaga.FlagVagaArquivada)
                            {
                                idsVagasAtivas.Add(objVagaIntegracaoBD.Vaga);
                            }
                            continue;
                        }

                        //Comparando conteúdo dos objetos. Se forem iguais, não faz alteração.
                        if (objVagaIntegracaoBD.Vaga.Equals(objVagaIntegracao.Vaga))
                        {
                            idsVagasAtivas.Add(objVagaIntegracaoBD.Vaga);
                            continue;
                        }

                        //Se funcao não foi identificada tenta detectar funcao através da string recebida.
                        if (objVagaIntegracao.Vaga.Funcao == null)
                        {
                            objVagaIntegracao.Vaga.Funcao = Funcao.DetectarFuncao(objVagaIntegracao.Vaga.DescricaoFuncao);
                        }

                        //Verificando se o histórico deve ser gravado
                        //Não grava histórico se a vaga já foi auditada ou se não foi auditada e continua com erros para a publicação automática
                        if (objVagaIntegracaoBD.Vaga.FlagAuditada.Value ||
                            (!objVagaIntegracaoBD.Vaga.FlagAuditada.Value && !string.IsNullOrEmpty(erroPublicacaoAutomatica)))
                        {
                            gravarHistorico = false;
                        }

                        //Verifica se a vaga pode ser publicada automaticamente e publica automaticamente caso esta já não esteja auditada.
                        if (string.IsNullOrEmpty(erroPublicacaoAutomatica) &&
                            !objVagaIntegracaoBD.FlagEnviadaParaAuditoria &&
                            !objVagaIntegracaoBD.Vaga.FlagAuditada.Value)
                        {
                            //Se vaga ainda não foi auditada, vai ser auditada agora
                            //Processo Publicador de vagas será iniciado
                            flgInicializaPublicadorDeVagas = true;
                            objVagaIntegracaoBD.Vaga.DataAbertura = DateTime.Now;
                            objVagaIntegracaoBD.Vaga.DataPrazo = DateTime.Now.AddDays(Convert.ToInt32(Parametro.RecuperaValorParametro(BLL.Enumeradores.Parametro.QuantidadeDiasPrazoVaga)));
                        }

                        //objVagaIntegracaoBD.Vaga.CompleteObject();
                        objVagaIntegracao.Vaga.Filial = objVagaIntegracaoBD.Vaga.Filial;
                        objVagaIntegracao.Vaga.UsuarioFilialPerfil = objVagaIntegracaoBD.Vaga.UsuarioFilialPerfil;

                        #region Verificando a inserção e exclusão de disponibilidades
                        var disponibilidadesSalvas = VagaDisponibilidade.ListarDisponibilidadesPorVaga(objVagaIntegracaoBD.Vaga);
                        disponibilidadesSalvas.ForEach(d => d.Disponibilidade.CompleteObject());
                        if (objVagaIntegracao.Disponibilidades != null)
                        {
                            foreach (var objVagaDisponibilidade in objVagaIntegracao.Disponibilidades)
                            {
                                if (!disponibilidadesSalvas.Exists(d => d.Disponibilidade.IdDisponibilidade == objVagaDisponibilidade.Disponibilidade.IdDisponibilidade))
                                {
                                    lstDisponibilidadesNovas.Add(objVagaDisponibilidade);
                                }
                            }
                        }
                        if (disponibilidadesSalvas != null && objVagaIntegracao.Disponibilidades != null)
                        {
                            foreach (var objVagaDisponibilidade in disponibilidadesSalvas)
                            {
                                if (!objVagaIntegracao.Disponibilidades.Exists(d => d.Disponibilidade.IdDisponibilidade == objVagaDisponibilidade.Disponibilidade.IdDisponibilidade))
                                {
                                    lstDisponibilidadesExcluidas.Add(objVagaDisponibilidade);
                                }
                            }
                        }
                        #endregion Verificando a inserção e exclusão de disponibilidades

                        #region Verificando a inserção e exclusão de tipo vinculo
                        var tipoVinculoSalvos = VagaTipoVinculo.ListarTipoVinculoPorVaga(objVagaIntegracaoBD.Vaga.IdVaga);
                        if (objVagaIntegracao.TiposVinculo != null)
                        {
                            foreach (var objVagaTipoVinculo in objVagaIntegracao.TiposVinculo)
                            {
                                if (!tipoVinculoSalvos.Exists(d => d.TipoVinculo.IdTipoVinculo == objVagaTipoVinculo.TipoVinculo.IdTipoVinculo))
                                {
                                    lstTipoVinculoNovos.Add(objVagaTipoVinculo);
                                }
                            }
                        }
                        if (tipoVinculoSalvos != null && objVagaIntegracao.TiposVinculo != null)
                        {
                            foreach (var objVagaTipoVinculo in tipoVinculoSalvos)
                            {
                                if (!objVagaIntegracao.TiposVinculo.Exists(d => d.TipoVinculo.IdTipoVinculo == objVagaTipoVinculo.TipoVinculo.IdTipoVinculo))
                                {
                                    lstTipoVinculoExcluidos.Add(objVagaTipoVinculo);
                                }
                            }
                        }
                        #endregion Verificando a inserção e exclusão de tipo vinculo

                        #endregion VagaIntegracaoCarregada
                    }

                    AtualizaInformacoesVaga(objVagaIntegracaoBD, objVagaIntegracao, origem);

                    #warning Favor arrumar isso e colocar na classe.

                    #region Trans. Atualiza Dados
                    var conn = new SqlConnection(DataAccessLayer.CONN_STRING);
                    conn.Open();
                    try
                    {
                        using (var trans = conn.BeginTransaction())
                        {
                            try
                            {
                                #region transacao

                                //Salvando todos os dados
                                /* DESCOMENTAR NA PUBLICAÇÃO DA BUSCA POR DISTÂNCIA
                                    * objVagaIntegracaoBD.Vaga.Endereco.Save(trans);*/
                                objVagaIntegracaoBD.Vaga.Save(trans, null, BLL.Enumeradores.VagaLog.ImportarVagas);
                                objVagaIntegracaoBD.Save(trans);

                                idsVagasAtivas.Add(objVagaIntegracaoBD.Vaga);

                                trans.Commit();
                                #endregion transacao
                            }
                            catch
                            {
                                trans.Rollback();
                                throw;
                            }
                        }
                    }
                    catch
                    {
                        throw;
                    }
                    finally
                    {
                        conn.Close();
                    }
                    #endregion Trans. Atualiza Dados

                    #region Salvando Listas de Disponibilidades e Vinculos
                    if (lstDisponibilidadesNovas != null)
                    {
                        foreach (var objVagaDisponibilidade in lstDisponibilidadesNovas)
                        {
                            try
                            {
                                objVagaDisponibilidade.Vaga = objVagaIntegracaoBD.Vaga;
                                objVagaDisponibilidade.Save();
                            }
                            catch (Exception ex)
                            {
                                var customMessage = string.Format("Erro ao gravar disponibilidade. URL: {0} Codigo Vaga: {1}", UrlIntegracao, objVagaIntegracao.CodigoVagaIntegrador);
                                GerenciadorException.GravarExcecao(ex);
                            }
                        }
                    }
                    if (lstDisponibilidadesExcluidas != null)
                    {
                        foreach (var objVagaDisponibilidade in lstDisponibilidadesExcluidas)
                        {
                            try
                            {
                                VagaDisponibilidade.Delete(objVagaDisponibilidade.IdVagaDisponibilidade);
                            }
                            catch (Exception ex)
                            {
                                var customMessage = string.Format("Erro ao excluir disponibilidade. URL: {0} Codigo Vaga: {1}", UrlIntegracao, objVagaIntegracao.CodigoVagaIntegrador);
                                GerenciadorException.GravarExcecao(ex);
                            }
                        }
                    }
                    if (lstTipoVinculoNovos != null)
                    {
                        foreach (var objTipoVinculo in lstTipoVinculoNovos)
                        {
                            try
                            {
                                objTipoVinculo.Vaga = objVagaIntegracaoBD.Vaga;
                                objTipoVinculo.Save();
                            }
                            catch (Exception ex)
                            {
                                var customMessage = string.Format("Erro ao gravar tipo vinculo. URL: {0} Codigo Vaga: {1}", UrlIntegracao, objVagaIntegracao.CodigoVagaIntegrador);
                                GerenciadorException.GravarExcecao(ex);
                            }
                        }
                    }
                    if (lstTipoVinculoExcluidos != null)
                    {
                        foreach (var objTipoVinculo in lstTipoVinculoExcluidos)
                        {
                            try
                            {
                                VagaTipoVinculo.Delete(objTipoVinculo.IdVagaTipoVinculo);
                            }
                            catch (Exception ex)
                            {
                                var customMessage = string.Format("Erro ao excluir tipo vinculo. URL: {0} Codigo Vaga: {1}", UrlIntegracao, objVagaIntegracao.CodigoVagaIntegrador);
                                GerenciadorException.GravarExcecao(ex);
                            }
                        }
                    }
                    #endregion Salvando Listas de Disponibilidades e Vinculos

                    #region Salvando Histórico para constar publicação
                    if (gravarHistorico)
                    {
                        if (string.IsNullOrEmpty(erroPublicacaoAutomatica))
                        {
                            HistoricoPublicacaoVaga.RegistrarHistorico(objVagaIntegracaoBD.Vaga.IdVaga, MensagemSucessoImportacao.Replace("{url}", UrlIntegracao).Replace("{data}", DateTime.Now.ToString("dd/MM/yy H:mm:ss")));
                        }
                        else
                        {
                            HistoricoPublicacaoVaga.RegistrarHistorico(objVagaIntegracaoBD.Vaga.IdVaga, MensagemErroImportacao.Replace("{erros}", erroPublicacaoAutomatica));
                        }
                    }
                    #endregion Salvando Histórico para constar publicação

                    #region Processo de Publicação Assincrona
                    //Se foi está pronta para a auditoria e o processo de publicação não foi iniciado, envia a vaga para o precesso do Publicador assincrono
                    if (flgInicializaPublicadorDeVagas && !objVagaIntegracaoBD.FlagEnviadaParaAuditoria)
                    {
                        objVagaIntegracaoBD.Publicar();

                        //Sinalizando que a vaga foi enviada para o processo de auditoria
                        objVagaIntegracaoBD.FlagEnviadaParaAuditoria = true;
                        objVagaIntegracaoBD.Save();
                    }
                    #endregion Processo de Publicação Assincrona
                }
                catch (Exception ex)
                {
                    var customMessage = string.Format("URL: {0} Codigo Vaga: {1}", objIntegrador.GetValorParametro(BLL.Enumeradores.Parametro.Integracao_Url_Integracao), objVagaIntegracao.CodigoVagaIntegrador);
                    GerenciadorException.GravarExcecao(ex, customMessage);
                }
            } // Fim loop de vagas do arquivo

            #region Apaga vagas do integrador não presentes no arquivo
            try
            {
                var FlagExcluirVagasNaoImportadas = objIntegrador.GetValorParametro(BLL.Enumeradores.Parametro.Integracao_Flg_Excluir_Vagas_Nao_Importadas);
                if (FlagExcluirVagasNaoImportadas == "1" || FlagExcluirVagasNaoImportadas == "true")
                {
                    objIntegrador.InativarVagas(idsVagasAtivas, objIntegrador);
                }
            }
            catch (Exception ex)
            {
                var customMessage = string.Format("Erro ao excluir vagas não impotadas. URL: {0}", UrlIntegracao);
                GerenciadorException.GravarExcecao(ex, customMessage);
            }
            #endregion Apaga vagas do integrador não presentes no arquivo
        }
        #endregion

