        /// <summary>
        ///     Verifica se erros ocorreram na importação da vaga.
        /// </summary>
        /// <returns>String com os erros. Se o retorno for vazio, nenhum erro foi encontrado.</returns>
        public static string VerificarPublicacaoAutomatica(VagaIntegracao objVagaIntegracao)
        {
            var retorno = new StringBuilder();
            string msg;
            if (objVagaIntegracao.Vaga.Funcao == null || objVagaIntegracao.Vaga.Funcao.IdFuncao <= 0)
            {
                msg = Parametro.RecuperaValorParametro(BLL.Enumeradores.Parametro.MensagemPublicacaoAutomaticaErroFuncao);
                msg = msg.Replace("{funcao}", objVagaIntegracao.FuncaoImportada);
                retorno.AppendLine(msg);
            }
            if (objVagaIntegracao.Vaga.Cidade == null || objVagaIntegracao.Vaga.Cidade.IdCidade <= 0)
            {
                msg = Parametro.RecuperaValorParametro(BLL.Enumeradores.Parametro.MensagemPublicacaoAutomaticaErroCidade);
                msg = msg.Replace("{cidade}", objVagaIntegracao.CidadeImportada);
                retorno.AppendLine(msg);
            }

            return retorno.ToString();
        }
